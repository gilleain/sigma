package net.torrance.gilleain.sigma;


/**
 * POJO the represents the isomorphism result.
 *
 */
public class IsomorphismResult {

  /**
   * The overall result - are the pair of graphs isomorphic or not?
   */
  private boolean isIsomorphic;

  public boolean isIsomorphic() {
    return isIsomorphic;
  }

  public void setIsomorphic(boolean isIsomorphic) {
    this.isIsomorphic = isIsomorphic;
  }



}

