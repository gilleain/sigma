package net.torrance.gilleain.sigma;


/**
 * POJO representing the pair of input graphs.
 */
public class GraphPair {

  private String graphA;

  private String graphB;

  public String getGraphA() {
    return graphA;
  }

  public void setGraphA(String graphA) {
    this.graphA = graphA;
  }

  public String getGraphB() {
    return graphB;
  }

  public void setGraphB(String graphB) {
    this.graphB = graphB;
  }

}

